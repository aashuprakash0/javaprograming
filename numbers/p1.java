package numbers;
//WAP to swap to numbers using additional and temporary variable 
public class p1 {
public static void main(String[] args) {
	int a=100; 
	int b=200;
	
	System.out.println("a="+a);
	System.out.println("b="+b);
	System.out.println("----------------------");
	int temp =a;
	a=b;
	b=temp; 
	System.out.println("a="+a);
	System.out.println("b="+b);
	
	
}
}
